def create_issue(title, description):
    url = f"https://gitlab.com/api/v4/projects/{PROJECT_ID}/issues"
    headers = {"PRIVATE-TOKEN": TOKEN}
    data = {"title": title, "description": description}
    response = requests.post(url, headers=headers, data=data)
    print("Issue created:", response.status_code)
    print("Response:", response.text)
    return response

def comment_on_mr(mr_iid, analysis):
    url = f"https://gitlab.com/api/v4/projects/{PROJECT_ID}/merge_requests/{mr_iid}/notes"
    headers = {"PRIVATE-TOKEN": TOKEN}
    body = f"""## OPERO Risk Analysis

**Failure type:** {analysis['failure']}
**Severity:** {analysis['severity']}
**Suggested fix:** {analysis['fix'][0] if analysis['fix'] else 'Investigate logs'}

> Auto-generated by OPERO incident triage agent
"""
    data = {"body": body}
    response = requests.post(url, headers=headers, data=data)
    print("MR comment posted:", response.status_code)

