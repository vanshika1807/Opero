def generate_rca(error, result):
    url = f"https://gitlab.com/api/v4/projects/{PROJECT_ID}/wikis"
    headers = {"PRIVATE-TOKEN": TOKEN}
    
    rca_content = f"""# RCA Report — {datetime.now().strftime('%Y-%m-%d %H:%M')}

## Incident Summary
- **Error:** {error}
- **Service:** Login API
- **Environment:** Production
- **Detected at:** {datetime.now()}

## Root Cause Analysis
- **Failure type:** {result['failure']}
- **Severity:** {result['severity']}

## Timeline
- {datetime.now()} — OPERO detected anomaly
- {datetime.now()} — Auto-triage completed
- {datetime.now()} — Issue created, team notified

## Resolution Steps
{chr(10).join(f'- {step}' for step in result['fix'])}

## Prevention
- Add monitoring alerts for this failure type
- Review runbook for {result['failure']}

> Auto-generated by OPERO
"""
    data = {
        "title": f"RCA-{datetime.now().strftime('%Y%m%d-%H%M')}",
        "content": rca_content,
        "format": "markdown"
    }
    response = requests.post(url, headers=headers, json=data)
    print("RCA report created:", response.status_code)

